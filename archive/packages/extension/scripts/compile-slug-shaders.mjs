/**
 * Offline Slug shader build step.
 * 1. Sync GLSL sources from three-text (Eric Lengyel MIT Slug port).
 * 2. Compile + link in headless Chromium (WebGL2) — fail the build on shader errors.
 * 3. Emit slug-shaders.generated.ts for the node-editor webview bundle.
 */
import * as fs from "node:fs";
import * as path from "node:path";
import { fileURLToPath } from "node:url";
import { chromium } from "playwright-core";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const extRoot = path.resolve(__dirname, "..");
const workspaceRoot = path.resolve(extRoot, "../..");
const shaderDir = path.join(extRoot, "src/node-editor/shaders");
const generatedTs = path.join(extRoot, "src/node-editor/webview/slug-shaders.generated.ts");
const outShaderDir = path.join(extRoot, "out/node-editor-webview/shaders");

function resolveModuleFile(...segments) {
  const candidates = [
    path.join(extRoot, "node_modules", ...segments),
    path.join(workspaceRoot, "node_modules", ...segments),
  ];
  const found = candidates.find((p) => fs.existsSync(p));
  if (!found) {
    throw new Error(`missing ${segments.join("/")} — run npm install in components/mmkit`);
  }
  return found;
}

function unescapeGlslString(literal) {
  return literal.replace(/\\n/g, "\n").replace(/\\"/g, '"');
}

function syncShaderSourcesFromThreeText() {
  const webglBundle = fs.readFileSync(
    resolveModuleFile("three-text/dist/vector/webgl/index.js"),
    "utf8"
  );
  const vertMatch = webglBundle.match(/var vertGLSL = "((?:\\.|[^"\\])*)";/);
  const fragMatch = webglBundle.match(/var fragGLSL = "((?:\\.|[^"\\])*)";/);
  if (!vertMatch || !fragMatch) {
    throw new Error("could not extract Slug GLSL from three-text/vector/webgl");
  }
  fs.mkdirSync(shaderDir, { recursive: true });
  const vertex = unescapeGlslString(vertMatch[1]);
  const fragment = unescapeGlslString(fragMatch[1]);
  fs.writeFileSync(path.join(shaderDir, "slug-vertex.glsl"), vertex);
  fs.writeFileSync(path.join(shaderDir, "slug-fragment.glsl"), fragment);
  return { vertex, fragment };
}

async function validateShadersInBrowser(vertexSource, fragmentSource) {
  const browser = await chromium.launch({ headless: true });
  try {
    const page = await browser.newPage();
    const error = await page.evaluate(
      ({ vertexSource: vs, fragmentSource: fsSrc }) => {
        const canvas = document.createElement("canvas");
        const gl = canvas.getContext("webgl2");
        if (!gl) return "WebGL2 is not available in the build browser";

        const compile = (type, source) => {
          const shader = gl.createShader(type);
          gl.shaderSource(shader, source);
          gl.compileShader(shader);
          if (!gl.getShaderParameter(shader, gl.COMPILE_STATUS)) {
            const log = gl.getShaderInfoLog(shader) ?? "unknown compile error";
            gl.deleteShader(shader);
            return log;
          }
          return shader;
        };

        const vsShader = compile(gl.VERTEX_SHADER, vs);
        if (typeof vsShader === "string") return `vertex shader: ${vsShader}`;
        const fsShader = compile(gl.FRAGMENT_SHADER, fsSrc);
        if (typeof fsShader === "string") {
          gl.deleteShader(vsShader);
          return `fragment shader: ${fsShader}`;
        }

        const program = gl.createProgram();
        gl.attachShader(program, vsShader);
        gl.attachShader(program, fsShader);
        gl.linkProgram(program);
        gl.deleteShader(vsShader);
        gl.deleteShader(fsShader);
        if (!gl.getProgramParameter(program, gl.LINK_STATUS)) {
          const log = gl.getProgramInfoLog(program) ?? "unknown link error";
          gl.deleteProgram(program);
          return `program link: ${log}`;
        }
        gl.deleteProgram(program);
        return null;
      },
      { vertexSource, fragmentSource }
    );
    if (error) {
      throw new Error(`Slug shader offline compile failed: ${error}`);
    }
  } finally {
    await browser.close();
  }
}

function withAdaptiveDefine(fragmentSource) {
  const versionEnd = fragmentSource.indexOf("\n");
  if (versionEnd === -1) {
    return `#define SLUG_ADAPTIVE_SUPERSAMPLE\n${fragmentSource}`;
  }
  return `${fragmentSource.slice(0, versionEnd + 1)}#define SLUG_ADAPTIVE_SUPERSAMPLE\n${fragmentSource.slice(versionEnd + 1)}`;
}

function emitGeneratedTs(vertex, fragment) {
  const esc = (s) => JSON.stringify(s);
  const adaptive = withAdaptiveDefine(fragment);
  const body = `/** Generated by scripts/compile-slug-shaders.mjs — do not edit. */
export const SLUG_VERTEX_SHADER = ${esc(vertex)};
export const SLUG_FRAGMENT_SHADER = ${esc(fragment)};
export const SLUG_FRAGMENT_SHADER_ADAPTIVE = ${esc(adaptive)};
`;
  fs.writeFileSync(generatedTs, body);
}

function copyShadersToOut(vertex, fragment) {
  fs.mkdirSync(outShaderDir, { recursive: true });
  fs.writeFileSync(path.join(outShaderDir, "slug-vertex.glsl"), vertex);
  fs.writeFileSync(path.join(outShaderDir, "slug-fragment.glsl"), fragment);
}

const { vertex, fragment } = syncShaderSourcesFromThreeText();
await validateShadersInBrowser(vertex, fragment);
await validateShadersInBrowser(vertex, withAdaptiveDefine(fragment));
emitGeneratedTs(vertex, fragment);
copyShadersToOut(vertex, fragment);
console.log("Slug shaders compiled offline:", generatedTs);
